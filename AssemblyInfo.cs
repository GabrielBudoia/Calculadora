## 🚀 Tecnologías

Este proyecto fue desarrollado con las siguientes tecnologías:

- C#
- .NET
- WPF
- XAML
- Git y GitHub

## 💻 Proyecto

Calculator App es una aplicación de escritorio que permite realizar operaciones matemáticas básicas de manera rápida y sencilla.

Funcionalidades incluidas:

- Suma
- Resta
- Multiplicación
- División
- Interfaz gráfica intuitiva

Este proyecto tiene como objetivo **practicar el desarrollo de aplicaciones de escritorio con C# y .NET**, aplicando conceptos de interfaz gráfica, eventos y lógica de programación.

## 🎨 Layout

El diseño de la aplicación está enfocado en la **simplicidad y facilidad de uso**, proporcionando una interfaz clara para realizar cálculos de forma rápida y eficiente.

